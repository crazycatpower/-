_DEADLINE_BY_MARKER = {"☆": "立即停工", "※": "即日改善"}


def official_marker_and_deadline(item_code: str) -> tuple[str, str]:
    """回傳某代碼在 PDF 上「固定」的 marker 與對應 deadline。
    這兩個值是官方表格本身定義的常數，不該由 LLM 決定，只能查表取得。
    """
    for items in CHECKLIST_1_TO_6.values():
        for it in items:
            if it.item == item_code:
                return it.marker, _DEADLINE_BY_MARKER.get(it.marker, "五日內改善")
    for items in OTHER_REFERENCE_ITEMS.values():
        for it in items:
            if it.item == item_code:
                return it.marker, _DEADLINE_BY_MARKER.get(it.marker, "五日內改善")
    return "", "五日內改善"


def official_text(item_code: str) -> str:
    """回傳某代碼在 PDF 上的官方條文原文，供報告顯示，讓使用者不需要另外查表
    就知道這個代碼實際在規範什麼（而不是只看模型自己的白話描述）。"""
    for items in CHECKLIST_1_TO_6.values():
        for it in items:
            if it.item == item_code:
                return it.text
    for items in OTHER_REFERENCE_ITEMS.values():
        for it in items:
            if it.item == item_code:
                return it.text
    return ""


def render_checklist_1_to_6() -> str:
    """把第1~6類（54條）官方項次與條文轉成 prompt 用的純文字清單。"""
    lines: list[str] = []
    for cat, items in CHECKLIST_1_TO_6.items():
        lines.append(f"【{cat}】")
        for it in items:
            marker_s = it.marker or "（一般，五日內改善）"
            lines.append(f"  {it.item} {marker_s} {it.text}")
    return "\n".join(lines)


def render_other_reference_items() -> str:
    """把第7~12類＋環境保護＋其他不符規定轉成「其他」類別引用用的純文字清單。"""
    lines: list[str] = []
    for cat, items in OTHER_REFERENCE_ITEMS.items():
        lines.append(f"【{cat}】")
        for it in items:
            marker_s = it.marker or "（一般）"
            lines.append(f"  {it.item} {marker_s} {it.text}")
    return "\n".join(lines)


